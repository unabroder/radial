public class TipoUsuario {

    private int idtipo;
    private String tipo;

    public TipoUsuario(int idtipo) {
        this.idtipo = idtipo;
    }

    public int getIdtipo() {
        return idtipo;
    }

    public void setIdtipo(int idtipo) {
        this.idtipo = idtipo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
